# what is docstring?
# holds the cursor on a function name, you will see the details of the function
# it is the first indented line of a python function that is enclosed between """docstring comment"""   #[3 double quotes]
def print_msg(name):
  """This function prints a message -- Look HERE, this is a docstring"""
  print(f"Hello {name}")

print_msg("foyez")

